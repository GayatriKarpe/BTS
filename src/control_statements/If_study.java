package control_statements;

public class If_study {

	public static void main(String[] args) {
		//if marks greater than 40 then print you are pass
		int marks=80;
		if(marks>40)
		{
			System.out.println("you are pass");
		}
		//if marks greater than or equal to 40
		int mark1=40;
		if(mark1>=40)
		{
			System.out.println("you are pass");
		}
		//if marks less than 40 type you are fail
		int mark2=8;
		if(mark2<40)
		{
			System.out.println("you are fail");
		}

	}

}
